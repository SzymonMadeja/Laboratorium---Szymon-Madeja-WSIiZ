print("7. ",type(11 % 2)) # % Operator modulo - Wypisanie tylko reszty z dzielenia podanych liczb
print("8. ",type(2 ** 10)) # ** Operator potęgowania
print("9. ",type(8 ** (1/3))) # ** Operator potęgowania, / Operator potęgowania z resztą a () oznacza, że najpierw wykona się rownanie w nich zawarte a pozniej dopiero reszta